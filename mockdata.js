const NOW = new Date("2026-04-18T10:30:00+02:00");
const iso = (d) => (d instanceof Date ? d : new Date(d)).toISOString();
const daysAgo = (n, h = 0, m = 0) => {
  const d = new Date(NOW);
  d.setDate(d.getDate() - n);
  if (h || m) d.setHours(h, m, 0, 0);
  return iso(d);
};
const daysAhead = (n, h = 0, m = 0) => {
  const d = new Date(NOW);
  d.setDate(d.getDate() + n);
  if (h || m) d.setHours(h, m, 0, 0);
  return iso(d);
};
const todayAt = (h, m = 0) => {
  const d = new Date(NOW);
  d.setHours(h, m, 0, 0);
  return iso(d);
};

